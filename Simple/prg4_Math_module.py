import math
#round down the decimal number to the nearest integer
print(math.floor(1.7))

# round up the decimal number to the nearest integer
print(math.ceil(4.1))

print(math.pi) # gives the value for pi

print(math.e) # gives the value for exponential

# It also includes trignometric functios which takes the numbers interms of radians
print(math.sin(math.pi / 2)) # givws the value in float

print(math.sin(math.pi)) # gives the value which is almost equal to zero
print(math.floor(math.sin(math.pi)))

# similarly we have math.cos and math.tan
# inverse functions
print(math.asin(0))
# similarly we have math.acos and math.atan

# to find the hypotenuse of a right angle triangle
print(math.hypot(3,4))

# power function
print(math.pow(2,3)) # gives 2^3.

# powe of an exponential function
print(math.exp(2)) # which gives e^2

# natural log function
print(math.log(math.e)) # natural log of an exponental is always 1.

# base 10 or base 2 function
print(math.log10(10))
print(math.log2(1))


